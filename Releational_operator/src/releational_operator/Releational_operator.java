package releational_operator;

public class Releational_operator {
	
	  public static void main(String[] args) {
		    
		    // create variables
		    int a = 12, b = 10;

		    // value of a and b
		    System.out.println("a is " + a + " and b is " + b);

		    // == operator
		    System.out.println(a == b);  
		    // != operator
		    System.out.println(a != b);  

		    // > operator
		    System.out.println(a > b);  

		    // < operator
		    System.out.println(a < b);  

		    // >= operator
		    System.out.println(a >= b);  

		    // <= operator
		    System.out.println(a <= b);  
		  }
		}
	
