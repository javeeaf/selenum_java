package arithmetic_operators;

public class Arithmetic_operators {

		  public static void main(String[] args) {
		    
		    // declare variables
		    int a = 10, b = 2;

		    // addition operator
		    System.out.println("a + b = " + (a + b));

		    // subtraction operator
		    System.out.println("a - b = " + (a - b));

		    // multiplication operator
		    System.out.println("a * b = " + (a * b));

		    // division operator
		    System.out.println("a / b = " + (a / b));

		    // modulo operator
		    System.out.println("a % b = " + (a % b));
		  }
		}