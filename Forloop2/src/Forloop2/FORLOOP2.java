package Forloop2;

public class FORLOOP2 {
	
	public static void main(String[] args) {
		
		int sumOfNaturalNumbers = 0; 		//initialize local variable
		for(int i =0; i<5;i++) {					
			sumOfNaturalNumbers += i; // sumOfNaturalNumbers = sumOfNaturalNumbers + i
									
		}
		
		System.out.println(sumOfNaturalNumbers);

		
	}
	

}
